/**
 * نرمال‌سازی متن فارسی برای جستجو
 *
 * مشکلاتی که حل می‌کند:
 * - ي عربی  →  ی فارسی
 * - ك عربی  →  ک فارسی
 * - أ إ ئ ء  →  ا / ی
 * - اعراب (فتحه، ضمه، کسره، ...)  →  حذف
 * - فاصله‌های اضافه  →  یک فاصله
 * - اعداد عربی/فارسی  →  لاتین (اختیاری)
 */
export function normalizePersian(input: string): string {
  if (!input) return '';

  return input
    // ── حروف عربی که با فارسی فرق دارند ────────────────────────────
    .replace(/ي/g, 'ی')          // يـ عربی  →  ی فارسی
    .replace(/ك/g, 'ک')          // كـ عربی  →  ک فارسی
    .replace(/[أإآ]/g, 'ا')      // همزه‌دار  →  الف ساده
    .replace(/ئ/g, 'ی')          // ئ  →  ی
    .replace(/ء/g, '')            // همزه جدا حذف
    .replace(/ة/g, 'ه')          // تاء مربوطه  →  ه
    .replace(/ؤ/g, 'و')          // واو همزه‌دار  →  و

    // ── اعراب (حرکات) ─────────────────────────────────────────────
    // U+064B–U+065F: تنوین، فتحه، ضمه، کسره، شدّه، سکون، ...
    .replace(/[\u064B-\u065F]/g, '')

    // ── فاصله‌های اضافه ────────────────────────────────────────────
    .replace(/\s+/g, ' ')
    .trim()

    // ── به حروف کوچک (برای Latin fallback) ────────────────────────
    .toLowerCase();
}
